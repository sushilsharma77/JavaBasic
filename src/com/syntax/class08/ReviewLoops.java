package com.syntax.class08;

public class ReviewLoops {

	public static void main(String[] args) {
		// write a program to calculate sum of odds and sum of even numbers
		//1 to 99
		int evenSum=0;
		int oddSum=0;
		for(int i=1; i<=99; i++) {
			
			if(i%2==0) {
 				System.out.println(evenSum=evenSum+i);
			}else {
				System.out.println(oddSum=oddSum+i);
			}
		}
			
		
		
		
		

	}

}

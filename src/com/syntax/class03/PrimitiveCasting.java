package com.syntax.class03;

public class PrimitiveCasting {

	public static void main(String[] args) {
		double d = 10;
		int i = (int) 10.99;
		System.out.println(i);
		byte b = (byte) 1284;

		System.out.println(b);

		float a = 100f;
		int f = (int) 10.99;
		System.out.println(a);
		System.out.println(f);
	}

}

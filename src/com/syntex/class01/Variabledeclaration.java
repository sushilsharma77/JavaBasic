package com.syntex.class01;

public class Variabledeclaration {

	public static void main(String[] args) {
		// creat a variable and store value
		// 1. declaring a variable and assigning a value
		boolean b = true;
		// 2. 1 step; declare variable
		// 2 step; assign value
		int i;
		i = 10000;
		// declare multiple variable and assign value later

		char x, y, z;
		x = 'A';
		y = '^';
		z = 'S';
		// we cannot have variables with same name in 1 program
		// double i=12.99;-->java will complain
		z = '#';
		// reassigning the value
		i = 2000;
		System.out.println(i);
		// declarin

	}

}
